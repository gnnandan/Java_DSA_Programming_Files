package _2_Conditional_Statements;

public class _2_if_else_statement
{
    public static void main(String[] args)
    {
        int a = 100;
        if(a==10)
        {
            System.out.println("True the value is 10");
        }
        else
        {
            System.out.println("False the value is not 10");
        }
    }
}
//output
//False the value is not 10