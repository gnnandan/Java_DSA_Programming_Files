package _2_Conditional_Statements;

public class _3_if_else_if_statement
{
    public static void main(String[] args)
    {
        int a = 1;
        if(a==10)
        {
            System.out.println("True the value is 10");
        }
        else if(a!=10)
        {
            System.out.println("True the value is not 10");
        }
        else
        {
            System.out.println("False the value is invalid");
        }
    }
}
//Output
//True the value is not 10
