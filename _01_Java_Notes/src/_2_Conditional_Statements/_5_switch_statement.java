package _2_Conditional_Statements;

public class _5_switch_statement
{
    public static void main(String[] args)
    {
        int input_num = 3;
        switch (input_num)
        {
            case 0:
                System.out.println("The number in case: " + input_num);
                break;
            case 1:
                System.out.println("The number in case: " + input_num);
                break;
            case 2:
                System.out.println("The number in case: " + input_num);
                break;
            case 3:
                System.out.println("The number in case: " + input_num);
                break;
            default:
                System.out.println("Invalid number!");
                break;
        }
    }
}
//Output
//The number in case: 3
