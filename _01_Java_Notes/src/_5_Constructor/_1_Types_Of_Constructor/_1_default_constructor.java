package _5_Constructor._1_Types_Of_Constructor;

public class _1_default_constructor
{
    //default constructor
    _1_default_constructor()
    {
        System.out.println("Default Constructor");
    }
    public static void main(String[] args)
    {
        new _1_default_constructor(); //object/instance creation
        System.out.println("Main Method");
    }
}
//output
//Default Constructor
//Main Method
